/****************************************************************************
 *                                                                          *
 *  FUNCTION   : TiffInfo()                                                 *
 *                                                                          *
 *  PURPOSE    : Count the number of pages in a TIFF file.                  *
 *                                                                          *
 ****************************************************************************/
int TiffInfo()
{
int i, iNumBytes, iTags, IFD;
int iPages;
FILE *iHandle;
unsigned char *buf;

   iPages = -1;
   iPage = 1; /* Start at page 1 */
   iHandle = fopen(szFileName, "rb");
   buf = malloc(4096);
   fread(buf, 1, 8, iHandle);
   if (buf[0] != 'I' && buf[0] != 'M')
      {
      if (buf[0] == 0xff && buf[1] == 0xd8)
	     {
		 pPagelist[0] = 0;
		 iPages = 1; // JPEG file
         }
	  goto tiffinfo_exit;
      }
   bMotorola = (buf[0] == 'M');
//   SetDlgItemText(hwnd, IDC_EDIT4, szByteOrder[bMotorola]); /* Show byte order */
   fseek(iHandle, 4, SEEK_SET); /* Read the first IFD pointer */
   fread(buf, 1, 4, iHandle);
   IFD = TIFFLONG(buf, bMotorola);
   iPages = 0;

   while(IFD) /* count the number of pages */
      {
      pPagelist[iPages] = IFD; /* Store pointer to start of each page */
      fseek(iHandle, IFD, SEEK_SET); /* Seek to this page */
      iNumBytes = fread(buf, 1, 3072, iHandle); /* Read a decent sized block */
      if (iNumBytes < 60)
         break; /* done, stop looking */
      iTags = TIFFSHORT(buf, bMotorola);  /* Number of tags in this dir */
      i = iTags * 12 + 2; /* Offset to next IFD in chain */
      if (i > iNumBytes) /* Invalid header */
         {
		 iPages = -1;
		 goto tiffinfo_exit; /* Bad header info */
         }

      IFD = TIFFLONG(buf+i, bMotorola);
	  iPages++;
      }
//   SetDlgItemInt(hwnd, IDC_EDIT5, iPage, FALSE);
//   SetDlgItemInt(hwnd, IDC_EDIT6, iPages, FALSE);

tiffinfo_exit:
   free(buf);
   fclose(iHandle);
   if (iPages > 0)
      FillIFDList();
   return iPages;

} /* TiffInfo() */

