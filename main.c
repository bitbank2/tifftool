void showFilename()
{
int i, iLen;

	iLen = strlen(szFileName);
	for (i=iLen-1; i>= 0; i--) // find the first separator character
	{
		if (szFileName[i] == '/')
			break;
	}
	if (i >= 0)
	{
		gtk_tool_button_set_label((GtkToolButton *)nameButton, &szFileName[i+1]);
	}
}

static void hexToggled(GtkWidget *widget, gpointer user_data)
{
	char *label;

	bHex = !bHex;
	label = "dec";
	if (bHex)
		label = "hex";
	gtk_tool_button_set_label((GtkToolButton *)hexdecButton, label);
	DoFile(); // re-display the info in hex/dec
}

static void
openClick (GtkWidget *widget, GtkWidget* pWindow)
{
GtkWidget *dialog;
GtkFileChooserAction action = GTK_FILE_CHOOSER_ACTION_OPEN;
gint res;

dialog = gtk_file_chooser_dialog_new ("Open File",
                                      window,
                                      action,
                                      "_Cancel",
                                      GTK_RESPONSE_CANCEL,
                                      "_Open",
                                      GTK_RESPONSE_ACCEPT,
                                      NULL);

res = gtk_dialog_run (GTK_DIALOG (dialog));
if (res == GTK_RESPONSE_ACCEPT)
  {
    char *filename;
    GtkFileChooser *chooser;

    chooser = GTK_FILE_CHOOSER (dialog);
    filename = gtk_file_chooser_get_filename (chooser);
    strcpy(szFileName, filename);
    g_free (filename);
    iPageTotal = TiffInfo(); // count the pages and confirm filetype
    showPage();
    showFilename();
    DoFile();
  }

gtk_widget_destroy (dialog);
} /* openClick() */

